CREATE TABLE `users`
( `ID` INT(10) NOT NULL, 
`FIRST_NAME` VARCHAR(100) NOT NULL, 
`LAST_NAME` VARCHAR(150) NOT NULL, 
`EMAIL` VARCHAR(200) NOT NULL, 
`PASSWORD` VARCHAR(200) CHARSET utf8 COLLATE utf8_general_ci NULL,
`SECOND_PASSWORD` VARCHAR(200) CHARSET utf8 COLLATE utf8_general_ci NULL,
`AGE` VARCHAR(250) CHARSET utf8 COLLATE utf8_general_ci NULL,
`GENDER` VARCHAR(40) NULL, 
`WEIGHT` VARCHAR(100), 
`HEIGHT` VARCHAR(100), 
`ANY_ALLERGIES` VARCHAR(2000), 
`HEALTH_ISSUES` VARCHAR(2000), 
`CONTACT_NO` VARCHAR(50) NOT NULL, 
`IS_ACTIVE` BOOLEAN DEFAULT 0  NULL,
`REGISTERED_ON` DATE NULL,
PRIMARY KEY (`id`) ); 